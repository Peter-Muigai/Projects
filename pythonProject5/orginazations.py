names = ['ben', 'mark', 'chris', 'marv', 'jess', 'anna']
names.sort()
print(names)

# Sort reverse

names = ['ben', 'mark', 'chris', 'marv', 'jess', 'anna']
names.sort(reverse=True)
print(names)

# Temporary sorting

names = ['ben', 'mark', 'chris', 'marv', 'jess', 'anna']
print("\n Here are the names in the original order:")
print(names)
print("\n Here is the sorted list:")
print(sorted(names))
print("\n Here is the original list again:")
print(names)
